
export const normailzarTexto = (text: string) => text
  .toUpperCase()
  .replace(/[脕脌脛脗]/g, "A")
  .replace(/[脡脠脣脢]/g, "E")
  .replace(/[脥脤脧脦]/g, "I")
  .replace(/[脫脪脰脭]/g, "O")
  .replace(/[脷脵脺脹]/g, "U")
  .replace(/[谩脿盲芒]/g, "A")
  .replace(/[茅猫毛锚]/g, "E")
  .replace(/[铆矛茂卯]/g, "I")
  .replace(/[贸貌枚么]/g, "O")
  .replace(/[煤霉眉没]/g, "U")
  .replace(/\*.*/g, "")
  .replace(/\?.*/g, "")
  .replace(/\(.*/g, "")
  .replace(/[,;]/g, " ")
  .replace(/\s+/g, " ")
  .trim().toLowerCase()